// Helper function to get universities by country
export const getUniversitiesByCountry = (countryName: string): string[] => {
  return UNIVERSITIES[countryName] || [];
};

// Helper function to search universities
export const searchUniversities = (query: string, countryName?: string): string[] => {
  const universities = countryName 
    ? getUniversitiesByCountry(countryName) 
    : Object.values(UNIVERSITIES).flat();
  
  return universities.filter(uni => 
    uni.toLowerCase().includes(query.toLowerCase())
  );
};
